package ba.unsa.etf.rpr.tutorijal4;

public class Smjer {
    private String nazivSmjera;
    private int smjerID;
    private int brojSemestra;

    public void setNazivSmjera(String nazivSmjera) {
        this.nazivSmjera = nazivSmjera;
    }

    public void setSmjerID(int smjerID) {
        this.smjerID = smjerID;
    }

    public void setBrojSemestra(int brojSemestra) {
        this.brojSemestra = brojSemestra;
    }

    public String getNazivSmjera() {
        return nazivSmjera;
    }

    public int getSmjerID() {
        return smjerID;
    }

    public int getBrojSemestra() {
        return brojSemestra;
    }




}